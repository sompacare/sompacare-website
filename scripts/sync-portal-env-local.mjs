#!/usr/bin/env node
/**
 * Sync apps/*-portal/.env.local from root .env.portals.local (one Clerk key set for all four).
 */
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");

const PORTALS = [
  {
    dir: "admin-portal",
    devTokenKey: "NEXT_PUBLIC_ADMIN_PORTAL_DEV_TOKEN",
    devTokenValue: "dev_admin",
    forceKey: "NEXT_PUBLIC_ADMIN_PORTAL_FORCE_DEV_TOKEN",
    stripe: false,
  },
  {
    dir: "nurse-portal",
    devTokenKey: "NEXT_PUBLIC_NURSE_PORTAL_DEV_TOKEN",
    devTokenValue: "dev_nurse_rn",
    forceKey: "NEXT_PUBLIC_NURSE_PORTAL_FORCE_DEV_TOKEN",
    stripe: true,
  },
  {
    dir: "facility-portal",
    devTokenKey: "NEXT_PUBLIC_FACILITY_PORTAL_DEV_TOKEN",
    devTokenValue: "dev_facility_mgr",
    forceKey: "NEXT_PUBLIC_FACILITY_PORTAL_FORCE_DEV_TOKEN",
    stripe: true,
  },
  {
    dir: "recruiter-portal",
    devTokenKey: "NEXT_PUBLIC_RECRUITER_PORTAL_DEV_TOKEN",
    devTokenValue: "dev_recruiter",
    forceKey: "NEXT_PUBLIC_RECRUITER_PORTAL_FORCE_DEV_TOKEN",
    stripe: false,
  },
];

const SHARED_KEYS = [
  "NEXT_PUBLIC_CLERK_PUBLISHABLE_KEY",
  "CLERK_SECRET_KEY",
  "NEXT_PUBLIC_CLERK_SIGN_IN_URL",
  "NEXT_PUBLIC_CLERK_SIGN_UP_URL",
  "NEXT_PUBLIC_CLERK_AFTER_SIGN_IN_URL",
  "NEXT_PUBLIC_CLERK_AFTER_SIGN_UP_URL",
  "NEXT_PUBLIC_CLERK_IS_SATELLITE",
  "NEXT_PUBLIC_API_URL",
  "NEXT_PUBLIC_STRIPE_PUBLISHABLE_KEY",
];

function parseEnv(text) {
  const out = {};
  for (const line of text.split(/\r?\n/)) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith("#")) continue;
    const eq = trimmed.indexOf("=");
    if (eq === -1) continue;
    const key = trimmed.slice(0, eq).trim();
    let val = trimmed.slice(eq + 1).trim();
    if ((val.startsWith('"') && val.endsWith('"')) || (val.startsWith("'") && val.endsWith("'"))) {
      val = val.slice(1, -1);
    }
    out[key] = val;
  }
  return out;
}

function loadSourceEnv() {
  const candidates = [
    path.join(root, ".env.portals.local"),
    path.join(root, ".env.local"),
    path.join(root, ".env.platform"),
  ];
  const merged = {};
  for (const file of candidates) {
    if (fs.existsSync(file)) Object.assign(merged, parseEnv(fs.readFileSync(file, "utf8")));
  }
  return merged;
}

function buildPortalEnv(shared, portal, forceDev) {
  const lines = [
    "# Generated by scripts/sync-portal-env-local.mjs — edit .env.portals.local then re-run npm run portals:sync-env",
    "",
  ];
  for (const key of SHARED_KEYS) {
    if (key === "NEXT_PUBLIC_STRIPE_PUBLISHABLE_KEY" && !portal.stripe) continue;
    const value = shared[key];
    if (!value) continue;
    lines.push(`${key}=${value}`);
  }
  lines.push("");
  lines.push(`${portal.devTokenKey}=${portal.devTokenValue}`);
  lines.push(`${portal.forceKey}=${forceDev ? "true" : "false"}`);
  lines.push("");
  return lines.join("\n");
}

const shared = loadSourceEnv();
const pk = shared.NEXT_PUBLIC_CLERK_PUBLISHABLE_KEY?.trim();
if (!pk?.startsWith("pk_") || pk.includes("REPLACE")) {
  console.error(
    "Missing valid NEXT_PUBLIC_CLERK_PUBLISHABLE_KEY in .env.portals.local (copy from .env.portals.local.example)."
  );
  process.exit(1);
}
if (!shared.CLERK_SECRET_KEY?.trim()?.startsWith("sk_")) {
  console.error("Missing CLERK_SECRET_KEY in .env.portals.local");
  process.exit(1);
}

if (!shared.NEXT_PUBLIC_API_URL?.trim()) {
  shared.NEXT_PUBLIC_API_URL = "/api/v1";
}

const forceDev =
  shared.PORTAL_FORCE_DEV_TOKEN === "true" || shared.PORTAL_FORCE_DEV_TOKEN === "1";

for (const portal of PORTALS) {
  const outPath = path.join(root, "apps", portal.dir, ".env.local");
  fs.writeFileSync(outPath, buildPortalEnv(shared, portal, forceDev), "utf8");
  console.log(`Wrote ${outPath} (FORCE_DEV_TOKEN=${forceDev})`);
}

console.log("\nRestart portal dev servers. For Clerk sign-in, use http://localhost:3001–3004 (not 127.0.0.1).");
console.log("Add redirect URLs: npm run clerk:local-urls");
